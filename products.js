function generateProductCard(product) {
    return `
        <div class="home-product-content" data-product-id="${product.id}">
            <a href="products-page.html?id=${product.id}">
                <div class="home-product-display">
                    <img src="${product.image1}" alt="${product.name}">
                </div>
                <div class="product-details-spacing">
                    <p>${product.name}</p>
                    <h3>NGN${product.price.toLocaleString()}</h3>
                </div>
            </a>
        </div>
    `;
}


function renderFilteredProducts(category) {
    // Containers for each category section and their titles
    const categorySections = {
        shirts: document.querySelector('.shirts'),
        tank: document.querySelector('.tank'),
        denim: document.querySelector('.denim'),
        headwear: document.querySelector('.headwear'),
        track: document.querySelector('.track'),
        joggers: document.querySelector('.joggers'),
        hoodie: document.querySelector('.hoodie'),
        slides: document.querySelector('.slides')
    };
    
    // Hide all sections by default
    Object.values(categorySections).forEach(section => {
        if (section) {  // Check if section is not null
            section.style.display = 'none';  // Hide section
        }
    });

    // Clear all containers before re-rendering
    Object.values(categorySections).forEach(section => {
        if (section) {  // Check if section is not null
            const container = section.querySelector('.products');
            if (container) {
                container.innerHTML = '';  // Clear all product containers
            }
        }
    });

    // Filter products by the selected category
    const filteredProducts = category === 'all' 
        ? allProducts 
        : allProducts.filter(product => product.category === category);

    // Render filtered products and display the relevant sections
    filteredProducts.forEach(product => {
        const productHTML = generateProductCard(product);
        
        const section = categorySections[product.category];
        if (section) {  // Check if section is not null
            section.style.display = 'block';  // Show only the relevant category section
            section.querySelector('.products').innerHTML += productHTML;
        }
    });

    // Special case for showing all products
    if (category === 'all') {
        Object.values(categorySections).forEach(section => {
            if (section) {  // Check if section is not null
                section.style.display = 'block';  // Show all sections if 'all' is clicked
            }
        });
    }

    
}




// Event listener for category buttons
document.querySelectorAll('.category a').forEach(button => {
    button.addEventListener('click', (event) => {
        event.preventDefault();  // Prevent the default link behavior
        
        // Remove 'active' class from all category links
        document.querySelectorAll('.category a').forEach(link => link.classList.remove('active'));
        
        // Add 'active' class to the clicked button
        button.classList.add('active');
        
        const category = button.getAttribute('data-category');
        renderFilteredProducts(category);
    });
});

// Call the function to initially render all products
document.addEventListener('DOMContentLoaded', () => renderFilteredProducts('all'));
